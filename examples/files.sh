# 1. Create a new folder
mkdir demo_folder
cd demo_folder

# 2. Create a new file
touch hello.txt

# 3. Write to the file
echo "Hello, Computer Science students!" > hello.txt

# 4. Rename the file
mv hello.txt greeting.txt

# 5. Open the file (in default editor, like TextEdit)
open greeting.txt

# (Optional) Wait for the student to see it open, then continue...

# 6. Read the file content in terminal
cat greeting.txt

# 7. Delete the file
rm greeting.txt

# 8. Go up one directory and delete the folder
cd ..
rmdir demo_folder
